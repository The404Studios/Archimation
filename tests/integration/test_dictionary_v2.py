# 1. Phrase count + size + compression ratio
# ---------------------------------------------------------------------------

def test_phrase_count_above_floor(built_artifact):
    """v2 has to expand templates into >= 5000 distinct phrases — that
    is the entire reason it exists vs the static dictionary."""
    pc = built_artifact["phrase_count"]
    if pc is None:
        pytest.skip(
            "CLI did not print 'Built N phrases' — cannot verify count "
            f"(stdout was: {built_artifact['stdout'][:400]!r})"
        )
    assert pc >= 5000, f"phrase count {pc} below 5000 floor"


def test_compressed_size_in_range(built_artifact):
    """Compressed artifact should sit in 10KB-500KB. A1's reference build
    is ~25KB at zstd level 19 over ~7K phrases (12x ratio) — so 10KB is
    a generous floor that catches "templates didn't expand at all" without
    flagging A1's normal output. Upper bound 500KB catches a compression
    regression or someone inlining the entire en_US wordlist."""
    size = built_artifact["path"].stat().st_size
    assert 10 * 1024 <= size <= 500 * 1024, (
        f"compressed artifact size {size} bytes outside [10KB, 500KB]"
    )


def test_compression_ratio_at_least_3x(built_artifact):
    """raw / compressed should be >= 3 — a sanity floor for zstd (or gzip)
    on highly repetitive English text. If the CLI doesn't emit raw/compressed
    sizes, fall back to recomputing from the file + a re-pickle of the
    in-memory map (skip if that also isn't possible)."""
    raw = built_artifact["raw_size"]
    comp = built_artifact["compressed_size"]
    if raw is not None and comp is not None and comp > 0:
        ratio = raw / comp
    elif built_artifact["ratio"] is not None:
        ratio = built_artifact["ratio"]
    else:
        pytest.skip("CLI did not emit raw/compressed sizes; cannot compute ratio")
    assert ratio >= 3.0, f"compression ratio {ratio:.2f}x below 3x floor"


# ---------------------------------------------------------------------------
# 2. Load latency: cold load < 1s, hot lookups < 1ms mean
# ---------------------------------------------------------------------------

def test_cold_load_under_one_second(built_artifact, monkeypatch):
    """First lookup pays the full unpickle + decompress; must complete
    in under 1 second on the test box."""
    # Point the runtime loader at our tmp artifact.
    monkeypatch.setenv("AICONTROL_DICTIONARY_V2_PATH", str(built_artifact["path"]))
    _reset_cache()

    t0 = time.perf_counter()
    dictionary_v2.lookup("test phrase")
    elapsed = time.perf_counter() - t0
    assert elapsed < 1.0, f"cold lookup took {elapsed*1000:.1f}ms (>1s)"


def test_hot_lookup_mean_under_one_ms(built_artifact, monkeypatch):
    """100 successive lookups after warmup must average < 1ms each.
    If they don't, runtime contusion routing will be the bottleneck."""
    monkeypatch.setenv("AICONTROL_DICTIONARY_V2_PATH", str(built_artifact["path"]))
    _reset_cache()
    # Prime the cache.
    dictionary_v2.lookup("warmup phrase")

    samples: list[float] = []
    phrase = "raise the volume"
    for _ in range(100):
        t0 = time.perf_counter()
        dictionary_v2.lookup(phrase)
        samples.append(time.perf_counter() - t0)

    mean_s = statistics.mean(samples)
    assert mean_s < 0.001, (
        f"mean hot lookup {mean_s*1000:.3f}ms exceeds 1ms ceiling "
        f"(median={statistics.median(samples)*1000:.3f}ms, "
        f"max={max(samples)*1000:.3f}ms)"
    )


