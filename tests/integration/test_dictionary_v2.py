@pytest.mark.parametrize("phrase,expected", ROUTES,
                         ids=[f"{exp}::{p}" for p, exp in ROUTES])
def test_route(phrase, expected, built_artifact, monkeypatch):
    """Each phrase must resolve to a handler containing the expected
    family.action substring. Substring (not equality) tolerates minor
    naming drift in Agent A1's compiler output."""
    monkeypatch.setenv("AICONTROL_DICTIONARY_V2_PATH", str(built_artifact["path"]))
    _reset_cache()

    r = dictionary_v2.lookup(phrase)
    assert r is not None, f"{phrase!r} returned None"
    assert isinstance(r, dict), f"{phrase!r} returned non-dict: {type(r).__name__}"
    assert "handler_type" in r, f"{phrase!r} missing handler_type: {r!r}"
    assert expected in r["handler_type"], (
        f"{phrase!r} -> {r['handler_type']!r}, wanted substring {expected!r}"
    )


def test_routes_cover_all_14_families(built_artifact, monkeypatch):
    """Belt-and-braces: assert the ROUTES list collectively touches all
    14 advertised families. Catches a regression where someone deletes
    a family from ROUTES without realising."""
    families = {
        "brightness", "audio", "media", "power", "system", "monitoring",
        "window", "workspace", "service", "driver", "script", "app",
        "game", "clipboard",
    }
    seen = {expected.split(".", 1)[0] for _, expected in ROUTES}
    missing = families - seen
    assert not missing, f"ROUTES list missing families: {sorted(missing)}"


