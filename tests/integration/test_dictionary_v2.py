def test_nonsense_returns_none(built_artifact, monkeypatch):
    """A genuinely meaningless phrase should not match anything at the
    default threshold; routing falls through to LLM/Markov."""
    monkeypatch.setenv("AICONTROL_DICTIONARY_V2_PATH", str(built_artifact["path"]))
    _reset_cache()
    r = dictionary_v2.lookup("xyzzy random nonsense plugh frobnicate")
    assert r is None, f"nonsense phrase matched: {r!r}"


def test_high_threshold_may_reject(built_artifact, monkeypatch):
    """At threshold=0.99 even a known phrase may be rejected — this is
    expected and lets the caller dial sensitivity. We tolerate either
    None or a return at or above the threshold."""
    monkeypatch.setenv("AICONTROL_DICTIONARY_V2_PATH", str(built_artifact["path"]))
    _reset_cache()
    try:
        r = dictionary_v2.lookup("raise the volume", threshold=0.99)
    except TypeError:
        pytest.skip("lookup() does not accept threshold kwarg in this build")
    if r is not None:
        assert r.get("confidence", 0.0) >= 0.99, (
            f"lookup returned below-threshold result: {r!r}"
        )


def test_default_threshold_routes_volume_up(built_artifact, monkeypatch):
    """At the default threshold (~0.7), a clean phrase must route."""
    monkeypatch.setenv("AICONTROL_DICTIONARY_V2_PATH", str(built_artifact["path"]))
    _reset_cache()
    r = dictionary_v2.lookup("raise the volume")
    assert r is not None, "default-threshold lookup of clean phrase returned None"
    assert "audio.volume_up" in r["handler_type"]


# ---------------------------------------------------------------------------
# 5. No-collision invariant: every phrase resolves to ONE handler
# ---------------------------------------------------------------------------

def test_no_phrase_maps_to_multiple_handlers(built_artifact, monkeypatch):
    """The compiler may legitimately produce overlapping templates, but
    the resolved phrase->handler map must be a function (one handler per
    phrase). If the underlying dict isn't directly inspectable, we skip
    rather than fail."""
    monkeypatch.setenv("AICONTROL_DICTIONARY_V2_PATH", str(built_artifact["path"]))
    _reset_cache()
    # Force load.
    dictionary_v2.lookup("warmup")

    # Try common attribute names for the resolved map.
    table = None
    for attr in ("PHRASES", "_PHRASES", "phrases", "_phrases",
                 "_TABLE", "_table", "DICTIONARY", "_DICTIONARY"):
        cand = getattr(dictionary_v2, attr, None)
        if isinstance(cand, dict) and cand:
            sample = next(iter(cand.values()))
            # Heuristic: handler entries are dicts with handler_type or
            # tuples with a string handler at index 0.
            if isinstance(sample, dict) and "handler_type" in sample:
                table = {k: v["handler_type"] for k, v in cand.items()}
                break
            if isinstance(sample, str):
                table = dict(cand)
                break
            if isinstance(sample, (list, tuple)) and sample \
                    and isinstance(sample[0], str):
                table = {k: v[0] for k, v in cand.items()}
                break

    if table is None:
        pytest.skip(
            "could not introspect resolved phrase table on dictionary_v2"
        )

    # In a function (one handler per phrase) duplicates are impossible by
    # construction since dict keys are unique, so what we really verify is
    # that NO phrase string is associated with multiple handler types
    # (which would only happen if the compiler returned a multimap).
    seen: dict[str, str] = {}
    collisions: list[tuple[str, str, str]] = []
    for phrase, handler in table.items():
        if phrase in seen and seen[phrase] != handler:
            collisions.append((phrase, seen[phrase], handler))
        seen[phrase] = handler

    assert not collisions, (
        f"phrase->handler collisions detected ({len(collisions)}): "
        f"first 5: {collisions[:5]}"
    )
