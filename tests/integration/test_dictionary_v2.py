@pytest.fixture(scope="module")
def built_artifact(tmp_path_factory):
    """Build the dictionary into a tmp file once per test module.

    Returns a dict with: path (Path), stdout (str), phrase_count (int|None),
    raw_size (int|None), compressed_size (int|None), ratio (float|None).
    """
    tmp = tmp_path_factory.mktemp("dict_v2")
    out_path = tmp / "dictionary_v2.pkl.zst"

    proc = subprocess.run(
        [sys.executable, "-m", "dictionary_v2", "--build", str(out_path)],
        capture_output=True,
        text=True,
        cwd=str(DAEMON_ROOT),
        timeout=120,
    )
    if proc.returncode != 0:
        pytest.fail(
            f"--build failed (rc={proc.returncode}):\n"
            f"STDOUT:\n{proc.stdout}\nSTDERR:\n{proc.stderr}"
        )
    if not out_path.exists():
        pytest.fail(f"--build returned 0 but artifact missing at {out_path}")

    out = proc.stdout + "\n" + proc.stderr  # be liberal about where CLI logs land

    # Parse "Built N phrases" — the CLI is contracted to print this line.
    m_count = re.search(r"Built\s+(\d+)\s+phrases", out, re.IGNORECASE)
    phrase_count = int(m_count.group(1)) if m_count else None

    # Parse the CLI's "302.4KB raw, 25.1KB compressed, 12.0x ratio" line.
    # Tolerate a few permutations so a future CLI tweak doesn't silently
    # skip the test.
    m_raw = (re.search(r"raw[\s=:]+(\d+)", out, re.IGNORECASE)
             or re.search(r"([\d.]+)\s*KB\s+raw", out, re.IGNORECASE))
    m_comp = (re.search(r"compressed[\s=:]+(\d+)", out, re.IGNORECASE)
              or re.search(r"([\d.]+)\s*KB\s+compressed", out, re.IGNORECASE))
    m_ratio = (re.search(r"ratio[\s=:]+([\d.]+)", out, re.IGNORECASE)
               or re.search(r"([\d.]+)x\s+ratio", out, re.IGNORECASE))
    # KB-form values are floats — promote to bytes when needed.
    def _bytes_or_int(m: Optional[re.Match]) -> Optional[int]:
        if m is None:
            return None
        s = m.group(1)
        try:
            f = float(s)
        except ValueError:
            return None
        # Heuristic: if the matched form was the "KB" alternative, the
        # number is in KB (often fractional); otherwise it's already bytes.
        if "." in s:
            return int(f * 1024)
        return int(f)
    raw_size = _bytes_or_int(m_raw)
    compressed_size = _bytes_or_int(m_comp)
    ratio = float(m_ratio.group(1)) if m_ratio else None

    return {
        "path": out_path,
        "stdout": out,
        "phrase_count": phrase_count,
        "raw_size": raw_size,
        "compressed_size": compressed_size,
        "ratio": ratio,
    }


def _reset_cache() -> None:
    """Best-effort reset of the module-level lookup cache.

    A1's module uses `_PHRASES`, `_LOAD_PATH`, `_LOAD_ERROR` as the lazy
    loader's state. Setting `_PHRASES = None` forces `_ensure_loaded()` to
    re-read the artifact from `AICONTROL_DICTIONARY_V2_PATH`. We also poke
    a few alternate names defensively in case the module gets refactored.
    """
    for attr in ("_PHRASES", "_LOAD_PATH", "_LOAD_ERROR",
                 "_cache", "_CACHE", "_loaded", "_LOADED",
                 "_dictionary", "_DICTIONARY"):
        if hasattr(dictionary_v2, attr):
            try:
                setattr(dictionary_v2, attr, None)
            except Exception:
                pass


